/* Operadores en JavaScript */

/* Aritmeticos + - * / % ()  */

const suma = function (numeroUno, numeroDos) {
    console.log(`La suma de los numeros ${numeroUno} y ${numeroDos} es: ${numeroUno + numeroDos}` )
}

const resta = function (numeroUno, numeroDos) {
    console.log(`La resta de los numeros ${numeroUno} y ${numeroDos} es: ${numeroUno - numeroDos}` )
}

const multiplicacion = function (numeroUno, numeroDos) {
    console.log(`La multiplicacion de los numeros ${numeroUno} y ${numeroDos} es: ${numeroUno * numeroDos}` )
}

const division = function (numeroUno, numeroDos) {
    console.log(`La division de los numeros ${numeroUno} y ${numeroDos} es: ${numeroUno / numeroDos}` )
}

const modulo = function (numeroUno, numeroDos) {
    console.log(`El modulo de los numeros ${numeroUno} y ${numeroDos} es: ${numeroUno % numeroDos}` )
}

/* Llamado de funciones */
suma(10,10)
resta(20,5)
multiplicacion(2,2)
division(5,2)
modulo(5,2)

